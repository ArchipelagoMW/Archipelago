def get_classification(item):
    try:
        if (item == "Spherical Atlas" or item == "Geographic Projection" or item == "Red Onion" or item == "Yellow Onion" or item == "Blue Onion"  or "Entrance Key" in item): # useful, placed early
            return ItemClassification.progression | ItemClassification.useful
        if ((item in explorer_kit_treasure and item != "The Key")): # still progression, can be placed in priority locations
            return ItemClassification.progression
        if (item == "Nothing" or item == "Ultra-Spicy Spray" or item == "Ultra-Bitter Spray"):
            return ItemClassification.filler
        return ItemClassification.progression | ItemClassification.deprioritized # everything else, still progression b/c debt, but will not be placed in priority locations if possible
    except AttributeError: # some form of backwards compatibility
        return ItemClassification.progression
    
class Pikmin2Item(Item):
    game: str = "Pikmin 2"
    def __init__(self, name, classification, code, player):
        for i in item_data:
            if (name == i[0]):
                self.text_id = i[1]
                self.value = i[4]
                self.weight = i[5]
                break
        super().__init__(name, classification, code, player)
    
    def get_value(self):
        if (self.name == "Victory"):
            return 0
        return [i[4] for i in item_data if i[0] == self.name][0]
    
    def get_weight(self):
        if (self.name == "Victory"):
            return 0
        return [i[5] for i in item_data if i[0] == self.name][0]