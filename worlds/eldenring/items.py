from dataclasses import dataclass
import dataclasses
from enum import IntEnum
from typing import Any, cast, ClassVar, Dict, Generator, List, Optional, Set

from BaseClasses import Item, ItemClassification

class ERItemCategory(IntEnum):
    GOODS = 1 # Misc, Key, Spell, most stuff that goes into your inventory
    WEAPON = 2
    ARMOR = 3
    ACCESSORY = 4
    ASHOFWAR = 5
    CUSTOMWEAPON = 6 # renforced and ash of wars

@dataclass
class ERItemData:
    __item_id: ClassVar[int] = 69000
    """The next item ID to use when creating item data."""

    name: str
    er_code: Optional[int]
    category: ERItemCategory

    base_name: Optional[str] = None
    """The name of the individual item, if this is a multi-item group."""

    classification: ItemClassification = ItemClassification.filler
    """How important this item is to the game progression."""

    ap_code: Optional[int] = None
    """The Archipelago ID for this item."""

    is_dlc: bool = False
    """Whether this item is only found in DLC."""

    count: int = 1
    """The number of copies of this item included in each drop."""

    runes: Optional[int] = None
    """If this is a consumable item that gives runes, the number of runes it gives."""

    filler: bool = False
    """Whether this is a candidate for a filler item to be added to fill out extra locations."""

    skip: bool = False
    """Whether to omit this item from randomization and replace it with filler or unique items."""

    def __post_init__(self):
        self.ap_code = self.ap_code or ERItemData.__item_id
        if not self.base_name: self.base_name = self.name
        ERItemData.__item_id += 1

    def item_groups(self) -> List[str]:
        """The names of item groups this item should appear in.

        This is computed from the properties assigned to this item."""
        names = []
        if self.classification == ItemClassification.progression: names.append("Progression")

        names.append({
            ERItemCategory.GOODS: "Goods",
            ERItemCategory.WEAPON: "Weapons",
            ERItemCategory.ARMOR: "Armor",
            ERItemCategory.ACCESSORY: "Accessories",
            ERItemCategory.ASHOFWAR: "Ash of war",
            ERItemCategory.CUSTOMWEAPON: "Upgraded Weapons",
        }[self.category])

        return names

    def counts(self, counts: List[int]) -> Generator["ERItemData", None, None]:
        """Returns an iterable of copies of this item with the given counts."""
        yield self
        for count in counts:
            yield dataclasses.replace(
                self,
                ap_code = None,
                name = "{} x{}".format(self.base_name, count),
                base_name = self.base_name,
                count = count,
                filler = False, # Don't count multiples as filler by default
            )


class ERItem(Item):
    game: str = "EldenRing"
    data: ERItemData

    def __init__(
            self,
            player: int,
            data: ERItemData,
            classification = None):
        super().__init__(data.name, classification or data.classification, data.ap_code, player)
        self.data = data

    @staticmethod #idk if this is needed
    def event(name: str, player: int) -> "ERItem":
        data = ERItemData(name, None, ERItemCategory.GOODS,
                           skip = True, classification = ItemClassification.progression)
        data.ap_code = None
        return ERItem(player, data)

