RegionNameCache = {}

def GetRegionName(stageId, index):
    stage_name = LEVEL_ID_TO_LEVEL[stageId]

    if (stageId, index) in RegionNameCache:
        return RegionNameCache[(stageId, index)]

    if index == 0:
        return f"{stage_name}_START"

    if index is None:
        #print("Unhandled index at this time")
        return f"{stage_name}_UNKNOWN"

    for name,lookup_index in REGION_INDICES.__dict__.items():
        if name.startswith(stage_name.upper().replace(" ","_")) and \
            index == lookup_index :
            RegionNameCache[(stageId, index)] = name
            return name

    # This is called in client for vehicles, etc in future fix, for now ignore
    print("Could not find region for", stageId, index)

    raise Exception("Invalid call")
    return None

