class DS3ItemData(NamedTuple):
    name: str
    ds3_code: int
    is_dlc: bool
    category: DS3ItemCategory


class DarkSouls3Item(Item):
    game: str = "Dark Souls III"

    @staticmethod
    def get_name_to_id() -> dict:
        base_id = 100000
        return {item_data.name: id for id, item_data in enumerate(_all_items, base_id)}


