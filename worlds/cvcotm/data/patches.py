    # Otherwise, run the "display one textbox" function.
    0x0E, 0x4A,  # ldr  r2, =0x805F104
    0x00, 0xE0,  # b    0x87FFF60
    0x0E, 0x4A,  # ldr  r2, =0x805F0C8
    0x7B, 0x46,  # mov  r3, r15
    0x05, 0x33,  # adds r3, #5
